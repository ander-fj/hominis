export function parseCSVFile(file: File): Promise<any[]> {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();

    reader.onload = (e) => {
      const text = e.target?.result as string;
      const lines = text.split('\n').filter(line => line.trim());

      if (lines.length === 0) {
        resolve([]);
        return;
      }

      const headers = lines[0].split(',').map(h => h.trim());
      const data = lines.slice(1).map(line => {
        const values = line.split(',').map(v => v.trim());
        const obj: any = {};
        headers.forEach((header, index) => {
          obj[header] = values[index];
        });
        return obj;
      });

      resolve(data);
    };

    reader.onerror = () => reject(new Error('Erro ao ler arquivo'));
    reader.readAsText(file);
  });
}

export function createXLSXTemplate(type: 'employees' | 'attendance' | 'sst'): string {
  const templates = {
    employees: 'name,email,department,position,hire_date',
    attendance: 'employee_email,date,status,hours_worked,delay_minutes',
    sst: 'employee_email,training_name,completion_date,expiry_date,status'
  };

  return templates[type];
}
